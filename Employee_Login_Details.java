    private void SelectIdBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SelectIdBtnActionPerformed
        ArrayList<LoginData> loginDetailsList=new ArrayList<>();
            
            String query="select * from Employee_Login_Table where Login_ID=? ";
            
            try {
            Class.forName(driver);
            Connection con=DriverManager.getConnection(url);          
            PreparedStatement st=con.prepareStatement(query);
            st.setString(1, searchid.getText()); 
            
            ResultSet rs=st.executeQuery();       
            LoginData login;
            
            while(rs.next())
            {
                login=new LoginData(rs.getString("Login_ID"),rs.getString("Login_Username"),rs.getString("Login_Password"),rs.getInt("Employee_ID"));
                loginDetailsList.add(login);
                   
            }
                        
            
             for(int i=0;i<loginDetailsList.size();i++)
            {     
              loginid.setText(loginDetailsList.get(i).getLoginid()); 
              username.setText(loginDetailsList.get(i).getUsername());
              password.setText(loginDetailsList.get(i).getPassword());
              empid.setText(String.valueOf(loginDetailsList.get(i).getEmpid()));
            }   
             
             
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Employee_Login_Details.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Employee_Login_Details.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_SelectIdBtnActionPerformed

