     public ArrayList<LoginData> loginList()
    {
        ArrayList<LoginData> loginList=new ArrayList<>();
        
        String query="select * from Employee_Login_Table";
        
        try {
            Class.forName(driver);
            Connection con=DriverManager.getConnection(url);
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery(query);
            LoginData login;
            
            while(rs.next())
            {
                login=new LoginData(rs.getString("Login_ID"),rs.getString("Login_Username"),rs.getString("Login_Password"),rs.getInt("Employee_ID"));
                loginList.add(login);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Employee_Login_Details.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Employee_Login_Details.class.getName()).log(Level.SEVERE, null, ex);
        }
        return loginList;
    }
    
     public void show_login_employee()
    {
        ArrayList<LoginData> list=loginList();
        DefaultTableModel model=(DefaultTableModel)jTable_Display_LoginData.getModel();
        Object[] row=new Object[4];
        for(int i=0;i<list.size();i++)
        {
            row[0]=list.get(i).getLoginid();
            row[1]=list.get(i).getUsername();
            row[2]=list.get(i).getPassword();
            row[3]=list.get(i).getEmpid();            
            model.addRow(row);
        }
    }
     
