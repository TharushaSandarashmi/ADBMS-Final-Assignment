    private void calculateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateBtnActionPerformed
         String sql="EXEC changeDesignation ?,?";
         
          try {
            Class.forName(driver);
            Connection con=DriverManager.getConnection(url);          
            PreparedStatement st=con.prepareStatement(sql);
            st.setString(1, employeeId.getText());
            st.setString(2, designation.getText());
            
            
            int result=st.executeUpdate();
            
            if(result>=1)
            {
                JOptionPane.showMessageDialog(null,"Designation is updated successfully!");
            }
            
            else
            {
                JOptionPane.showMessageDialog(null,"Error!");
            }
          } catch (ClassNotFoundException ex) {
            Logger.getLogger(Change_Designation.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Change_Designation.class.getName()).log(Level.SEVERE, null, ex);
        }
                    
          employeeId.setText("");
          designation.setText("");
          
    }//GEN-LAST:event_calculateBtnActionPerformed

